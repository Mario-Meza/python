"""
    union() es una función de Python que se usa para unir dos conjuntos. Esta función
    devuelve un nuevo conjunto que contiene los elementos de los dos conjuntos origi-
    nales.
"""

conjunto_1 = {1, 2, 3, 4}
conjunto_2 = {3, 4, 5, 6}

conjunto_unido = conjunto_1.union(conjunto_2)
print(conjunto_unido)