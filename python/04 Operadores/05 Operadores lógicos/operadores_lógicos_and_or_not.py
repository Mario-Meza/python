# and
print(True and True)
print(False and True)
print(True and False)
print(False and True)

a = 5
b = 10

print(a > 0 and b > 0)

# or
print(True or True)
print(False or True)
print(True or False)
print(False or False)

a = 5
b = -10

print(a > 0 or b > 0)

# not
print(not True)
print(not False)

a = 5
b = -10

print(not (a > 0 and b > 0))