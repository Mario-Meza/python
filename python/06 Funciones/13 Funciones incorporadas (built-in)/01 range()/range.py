"""
    - .range() es una funcion que nos permite generar una secuencia de numeros.Esta funcion recibe commo parametros:
      > 1: Numero inicial: Es el primer numero de la secuencia
      > 2: Numero final: Es el ultimo numero de la secuencia que queremos que llegue
      > 3: El paso es el numero de incremento entre cada numero de la secuencia.
"""
