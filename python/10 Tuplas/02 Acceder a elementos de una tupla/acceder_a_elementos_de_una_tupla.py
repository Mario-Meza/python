"""
    Acceder a elementos de una tupla en Python es muy sencillo. Una tupla es una
    secuencia de valores separados por comas, entre paréntesis.
    > En Python, cuando se utiliza la notación de corte (slicing) como [1:5].
    > Imprimirá los elementos de la tupla desde la posición 1 hasta la posición 4
"""
mi_tupla = (1,2,3,4,5)
print(mi_tupla[2])
print(mi_tupla[1:5])# (2, 3, 4, 5)
print(mi_tupla[:2])